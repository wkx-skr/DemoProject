      this.ps = new NewPs(listContainer)
    })
    $(window).on('resize', this.handleResize)
  },
  beforeDestroy() {
    if (this.ps) {
      this.ps.destroy()
    }
    $(window).off('resize', this.handleResize)
  },
  methods: {
    handleClose() {
      this.$emit('close')
    },
    handleResize() {
      if (this.ps) {
        this.$nextTick(() => {
          this.ps.update()
        })
      }
    },
  },
}
